#include "sys.h"
#include "sccb.h"
#include "delay.h"

void SCCB_Init ( void ) { /* 初始化SCCB接口，“CHECK OK” */
    RCC->APB2ENR |= 1 << 4; /* 先使能外设PORTC时钟 */
    /* PC4和5，OUT */
    GPIOC->CRL &= 0XFF00FFFF;
    GPIOC->CRL |= 0X00330000;
    GPIOC->ODR |= 3 << 4;
    SCCB_SDA_OUT();
}

/* SCCB起始信号；当时钟为高的时候，数据线从高到低为SCCB起始信号。在激活状态下，SDA和SCL均为低电平 */
void SCCB_Start ( void ) {
    SCCB_SDA = 1; /* 数据线高电平 */
    SCCB_SCL = 1; /* 在时钟线高的时候数据线由高至低 */
    delay_us ( 50 );
    SCCB_SDA = 0;
    delay_us ( 50 );
    SCCB_SCL = 0; /* 数据线恢复低电平，单操作函数必要 */
}

void SCCB_Stop ( void ) { /* SCCB停止信号；当时钟为高的时候，数据线从低到高为SCCB停止信号 */
    SCCB_SDA = 0;
    delay_us ( 50 );
    SCCB_SCL = 1;
    delay_us ( 50 );
    SCCB_SDA = 1;
    delay_us ( 50 );
}

void SCCB_No_Ack ( void ) { /* 产生NA信号 */
    delay_us ( 50 );
    SCCB_SDA = 1;
    SCCB_SCL = 1;
    delay_us ( 50 );
    SCCB_SCL = 0;
    delay_us ( 50 );
    SCCB_SDA = 0;
    delay_us ( 50 );
}

u8 SCCB_WR_Byte ( u8 dat ) { /* SCCB写入一个字节；返回0为成功，1为失败 */
    u8 j, res;

    for ( j = 0; j < 8; j++ ) { /* 循环8次发送数据 */
        if ( dat & 0x80 ) {
            SCCB_SDA = 1;
        } else {
            SCCB_SDA = 0;
        }

        dat <<= 1;
        delay_us ( 50 );
        SCCB_SCL = 1;
        delay_us ( 50 );
        SCCB_SCL = 0;
    }

    SCCB_SDA_IN(); /* 设置SDA为输入 */
    delay_us ( 50 );
    SCCB_SCL = 1; /* 接收第九位，以判断是否发送成功 */
    delay_us ( 50 );

    if ( SCCB_READ_SDA ) {
        res = 1; /* 发送失败返回1 */
    } else {
        res = 0; /* 发送成功返回0 */
    }

    SCCB_SCL = 0;
    SCCB_SDA_OUT(); /* 设置SDA为输出 */
    return res;
}

u8 SCCB_RD_Byte ( void ) {
    u8 temp = 0, j;
    SCCB_SDA_IN(); /* 设置SDA为输入 */

    for ( j = 8; j > 0; j-- ) { /* 循环8次接收数据 */
        delay_us ( 50 );
        SCCB_SCL = 1;
        temp = temp << 1;

        if ( SCCB_READ_SDA ) {
            temp++;
        }

        delay_us ( 50 );
        SCCB_SCL = 0;
    }

    SCCB_SDA_OUT(); /* 设置SDA为输出 */
    return temp;
}

u8 SCCB_WR_Reg ( u8 reg, u8 data ) { /* 写寄存器，返回0为成功，1为失败 */
    u8 res = 0;
    SCCB_Start(); /* 启动SCCB传输 */

    if ( SCCB_WR_Byte ( SCCB_ID ) ) {
        res = 1; /* 写器件ID */
    }

    delay_us ( 100 );

    if ( SCCB_WR_Byte ( reg ) ) {
        res = 1; /* 写寄存器地址 */
    }

    delay_us ( 100 );

    if ( SCCB_WR_Byte ( data ) ) {
        res = 1; /* 写数据 */
    }

    SCCB_Stop();
    return  res;
}

u8 SCCB_RD_Reg ( u8 reg ) { /* 读寄存器。返回读到的寄存器值 */
    u8 val = 0;
    SCCB_Start(); /* 启动SCCB传输 */
    SCCB_WR_Byte ( SCCB_ID ); /* 写器件ID */
    delay_us ( 100 );
    SCCB_WR_Byte ( reg ); /* 写寄存器地址 */
    delay_us ( 100 );
    SCCB_Stop();
    delay_us ( 100 );
    SCCB_Start();
    SCCB_WR_Byte ( SCCB_ID | 0X01 ); /* 发送读命令 */
    delay_us ( 100 );
    val = SCCB_RD_Byte(); /* 读取数据 */
    SCCB_No_Ack();
    SCCB_Stop();
    return val;
}